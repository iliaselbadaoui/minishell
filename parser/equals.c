#include "parser.h"

int		equals(t_string s1, t_string s2)
{
	if (!s1 || !s2)
		return (0);
	if (s1 == s2)
		return (1);
	while (*s1 && *s2)
	{
		if (*s1 != *s2)
			return (0);
		s1++;
		s2++;
	}
	if (*s1 != *s2)
		return (0);
	return (1);
}
